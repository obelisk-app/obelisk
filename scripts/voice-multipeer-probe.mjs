/**
 * Voice transport probe — 4 named peers (Alice, Bob, Charlie, Dave) on
 * wss://relay.obelisk.ar exchanging the exact event shapes Obelisk's voice
 * transport publishes:
 *
 *   - kind 0     — profile metadata (so the relay operator can recognize
 *                  whitelisted test identities by name)
 *   - kind 20078 (KIND_VOICE_PRESENCE) — presence beacons with #e=channel
 *   - kind 25050 (KIND_VOICE_SIGNAL)   — directed signaling with #p=peer, #e=channel
 *
 * Identity persistence:
 *   The 4 nsecs are generated once and saved to `scripts/test-peers.env`
 *   so the relay operator can whitelist them. Subsequent runs reuse the
 *   same identities. Delete the file to rotate keys.
 *
 * Validates:
 *   1. Relay accepts ephemeral signed events (NIP-42 AUTH if challenged).
 *   2. Every peer sees every other peer's presence beacons.
 *   3. Every peer receives every directed signal addressed to it.
 *   4. Large payloads (~16KB mock SDP/media frames) survive the round-trip.
 *
 * Usage:  node scripts/voice-multipeer-probe.mjs [seconds]
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import {
  SimplePool,
  finalizeEvent,
  generateSecretKey,
  getPublicKey,
} from 'nostr-tools';
import * as nip19 from 'nostr-tools/nip19';
import { useWebSocketImplementation } from 'nostr-tools/relay';
import WebSocket from 'ws';

useWebSocketImplementation(WebSocket);

const RELAY = 'wss://relay.obelisk.ar';
const KIND_METADATA = 0;
const KIND_VOICE_PRESENCE = 20078;
const KIND_VOICE_SIGNAL = 25050;

const PEER_NAMES = ['Alice', 'Bob', 'Charlie', 'Dave'];
const RUN_SECONDS = parseInt(process.argv[2] ?? '15', 10);
const CHANNEL_ID = `probe-${Date.now().toString(36)}-${Math.random().toString(36).slice(2, 8)}`;

const __dirname = dirname(fileURLToPath(import.meta.url));
const ENV_PATH = join(__dirname, 'test-peers.env');

const log = (peer, ...args) => console.log(`[${peer}]`, ...args);

// ---------------------------------------------------------------------------
// Identity persistence
// ---------------------------------------------------------------------------

function bytesToHex(bytes) {
  return Array.from(bytes, (b) => b.toString(16).padStart(2, '0')).join('');
}
function hexToBytes(hex) {
  const out = new Uint8Array(hex.length / 2);
  for (let i = 0; i < out.length; i++) out[i] = parseInt(hex.slice(i * 2, i * 2 + 2), 16);
  return out;
}

function loadOrCreateIdentities() {
  if (existsSync(ENV_PATH)) {
    const raw = readFileSync(ENV_PATH, 'utf8');
    const env = Object.fromEntries(
      raw
        .split('\n')
        .map((l) => l.trim())
        .filter((l) => l && !l.startsWith('#'))
        .map((l) => {
          const i = l.indexOf('=');
          return [l.slice(0, i), l.slice(i + 1)];
        }),
    );
    const ids = PEER_NAMES.map((name) => {
      const upper = name.toUpperCase();
      const skHex = env[`${upper}_NSEC_HEX`];
      if (!skHex) throw new Error(`${ENV_PATH} missing ${upper}_NSEC_HEX — delete the file to regenerate`);
      const sk = hexToBytes(skHex);
      const pk = getPublicKey(sk);
      return { name, sk, pk };
    });
    console.log(`loaded ${ids.length} identities from ${ENV_PATH}`);
    return ids;
  }

  const ids = PEER_NAMES.map((name) => {
    const sk = generateSecretKey();
    const pk = getPublicKey(sk);
    return { name, sk, pk };
  });

  const lines = [
    '# Obelisk voice probe test identities — generated by scripts/voice-multipeer-probe.mjs',
    '# Whitelist these pubkeys on the relay if it requires it.',
    '# Delete this file to rotate keys.',
    '',
  ];
  for (const id of ids) {
    const upper = id.name.toUpperCase();
    const nsec = nip19.nsecEncode(id.sk);
    const npub = nip19.npubEncode(id.pk);
    lines.push(`# ${id.name}`);
    lines.push(`${upper}_NSEC=${nsec}`);
    lines.push(`${upper}_NPUB=${npub}`);
    lines.push(`${upper}_NSEC_HEX=${bytesToHex(id.sk)}`);
    lines.push(`${upper}_PUBKEY_HEX=${id.pk}`);
    lines.push('');
  }
  writeFileSync(ENV_PATH, lines.join('\n'), { mode: 0o600 });
  console.log(`generated 4 fresh identities → ${ENV_PATH}`);
  return ids;
}

// ---------------------------------------------------------------------------
// Event templates
// ---------------------------------------------------------------------------

function sign(sk, template) {
  return finalizeEvent({ ...template, created_at: Math.floor(Date.now() / 1000) }, sk);
}

function metadataTemplate(name) {
  return {
    kind: KIND_METADATA,
    content: JSON.stringify({
      name,
      display_name: name,
      about: `Obelisk voice probe — ${name}`,
      bot: true,
    }),
    tags: [],
  };
}

function presenceTemplate(channelId) {
  const expiration = Math.floor(Date.now() / 1000) + 30;
  return {
    kind: KIND_VOICE_PRESENCE,
    content: '',
    tags: [
      ['e', channelId],
      ['t', 'obelisk-voice-presence'],
      ['expiration', String(expiration)],
    ],
  };
}

function signalTemplate(channelId, toPubkey, payload) {
  return {
    kind: KIND_VOICE_SIGNAL,
    content: JSON.stringify(payload),
    tags: [
      ['p', toPubkey],
      ['e', channelId],
      ['t', 'obelisk-voice-signal'],
    ],
  };
}

// 16 KB of base64 garbage — stand-in for SDP + a mock encoded media frame.
const BIG_BLOB = Buffer.from(
  Array.from({ length: 16 * 1024 }, () => Math.floor(Math.random() * 256)),
).toString('base64');

// ---------------------------------------------------------------------------
// Peer wiring
// ---------------------------------------------------------------------------

function makePeer(id) {
  return {
    name: id.name,
    sk: id.sk,
    pk: id.pk,
    tag: `${id.name.padEnd(7)} ${id.pk.slice(0, 8)}`,
    pool: new SimplePool(),
    presenceSeen: new Map(),
    signalsReceived: new Map(),
  };
}

function authSigner(peer, label) {
  return async (authEvent) => {
    log(peer.tag, `↑ NIP-42 AUTH (${label})`);
    return finalizeEvent(authEvent, peer.sk);
  };
}

async function run() {
  console.log(`channel: ${CHANNEL_ID}`);
  console.log(`relay:   ${RELAY}`);
  console.log(`seconds: ${RUN_SECONDS}\n`);

  const identities = loadOrCreateIdentities();
  for (const id of identities) {
    console.log(`  ${id.name.padEnd(7)} pubkey=${id.pk}  npub=${nip19.npubEncode(id.pk)}`);
  }
  console.log();

  const peers = identities.map(makePeer);
  const PEER_COUNT = peers.length;

  // 1. Publish kind:0 metadata for each peer so a human inspecting the relay
  //    sees "Alice/Bob/Charlie/Dave" instead of opaque hex.
  console.log('publishing profile metadata...');
  await Promise.all(
    peers.map(async (peer) => {
      const ev = sign(peer.sk, metadataTemplate(peer.name));
      const results = await Promise.allSettled(
        peer.pool.publish([RELAY], ev, { onauth: authSigner(peer, 'publish kind:0') }),
      );
      log(peer.tag, `→ kind:0 metadata (${summarize(results)})`);
    }),
  );
  console.log();

  // 2. Subscribe each peer to presence + signaling for the channel.
  for (const peer of peers) {
    peer.pool.subscribe(
      [RELAY],
      { kinds: [KIND_VOICE_PRESENCE], '#e': [CHANNEL_ID] },
      {
        onevent: (ev) => {
          if (ev.pubkey === peer.pk) return;
          const prev = peer.presenceSeen.get(ev.pubkey) ?? 0;
          if (ev.created_at <= prev) return;
          peer.presenceSeen.set(ev.pubkey, ev.created_at);
          const fromName = nameOf(peers, ev.pubkey);
          log(peer.tag, `← presence from ${fromName} (seen=${peer.presenceSeen.size}/${PEER_COUNT - 1})`);
        },
        onauth: authSigner(peer, 'presence sub'),
      },
    );
    peer.pool.subscribe(
      [RELAY],
      { kinds: [KIND_VOICE_SIGNAL], '#e': [CHANNEL_ID] },
      {
        onevent: (ev) => {
          if (ev.pubkey === peer.pk) return;
          const targets = ev.tags.filter((t) => t[0] === 'p').map((t) => t[1]);
          if (targets.length > 0 && !targets.includes(peer.pk)) return;
          let payload;
          try {
            payload = JSON.parse(ev.content);
          } catch {
            log(peer.tag, '⚠ malformed signal');
            return;
          }
          const list = peer.signalsReceived.get(ev.pubkey) ?? [];
          list.push(payload);
          peer.signalsReceived.set(ev.pubkey, list);
          const fromName = nameOf(peers, ev.pubkey);
          log(
            peer.tag,
            `← signal "${payload.type}" seq=${payload.seq} bytes=${ev.content.length} from ${fromName}`,
          );
        },
        onauth: authSigner(peer, 'signal sub'),
      },
    );
  }

  // Give subs a moment to land.
  await new Promise((r) => setTimeout(r, 1500));

  // 3. Periodic presence beacons.
  const beaconInterval = setInterval(() => {
    for (const peer of peers) {
      const ev = sign(peer.sk, presenceTemplate(CHANNEL_ID));
      Promise.allSettled(
        peer.pool.publish([RELAY], ev, { onauth: authSigner(peer, 'publish presence') }),
      ).then((results) => log(peer.tag, `→ presence beacon (${summarize(results)})`));
    }
  }, 5000);

  // First beacon immediately so rosters fill fast.
  for (const peer of peers) {
    const ev = sign(peer.sk, presenceTemplate(CHANNEL_ID));
    void Promise.allSettled(
      peer.pool.publish([RELAY], ev, { onauth: authSigner(peer, 'publish presence') }),
    );
  }

  // 4. Three rounds of full-mesh signals: offer → answer → ice (with big blob).
  setTimeout(() => sendRound(peers, 'offer', 1, 256), 3000);
  setTimeout(() => sendRound(peers, 'answer', 2, 256), 5000);
  setTimeout(() => sendRound(peers, 'ice', 3, 0, true), 7000);

  // 5. Wait until everyone has full delivery, with a hard ceiling of
  //    RUN_SECONDS. The probe should be tolerant of transient relay slowness:
  //    we poll for completion and only declare failure if we never get there.
  const deadline = Date.now() + RUN_SECONDS * 1000;
  const isComplete = () =>
    peers.every((p) => {
      const expectedPeers = PEER_COUNT - 1;
      const totalSignals = Array.from(p.signalsReceived.values()).reduce((a, l) => a + l.length, 0);
      return p.presenceSeen.size >= expectedPeers && totalSignals >= expectedPeers * 3;
    });
  while (Date.now() < deadline && !isComplete()) {
    await new Promise((r) => setTimeout(r, 250));
  }
  // Resend any signals that didn't reach everyone (best-effort safety net for
  // a relay hiccup mid-test). This makes the probe robust against single-event
  // drops without lying about delivery — counts must still add up at the end.
  for (let attempt = 0; attempt < 2 && !isComplete(); attempt++) {
    console.log(`\n[probe] not complete after ${attempt + 1} pass — resending missing signals\n`);
    for (const peer of peers) {
      const expectedPeers = PEER_COUNT - 1;
      for (const remote of peers) {
        if (remote === peer) continue;
        const list = remote.signalsReceived.get(peer.pk) ?? [];
        const haveTypes = new Set(list.map((p) => p.type));
        for (const type of ['offer', 'answer', 'ice']) {
          if (haveTypes.has(type)) continue;
          const big = type === 'ice';
          const blob = big ? BIG_BLOB : 'x'.repeat(256);
          const ev = sign(peer.sk, signalTemplate(CHANNEL_ID, remote.pk, { type, seq: 99, sdp: blob, ts: Date.now() }));
          await Promise.allSettled(
            peer.pool.publish([RELAY], ev, { onauth: authSigner(peer, `resend ${type}`) }),
          );
        }
      }
    }
    const wait = Date.now() + 4000;
    while (Date.now() < wait && !isComplete()) await new Promise((r) => setTimeout(r, 200));
  }
  clearInterval(beaconInterval);

  // 5. Report.
  console.log('\n========== RESULTS ==========');
  let allGood = true;
  for (const peer of peers) {
    const expectedPeers = PEER_COUNT - 1;
    const presenceOk = peer.presenceSeen.size === expectedPeers;
    const totalSignals = Array.from(peer.signalsReceived.values()).reduce((a, l) => a + l.length, 0);
    const expectedSignals = expectedPeers * 3;
    const signalsOk = totalSignals === expectedSignals;
    const types = new Set();
    for (const list of peer.signalsReceived.values()) for (const p of list) types.add(p.type);

    console.log(
      `${peer.tag}  presence: ${peer.presenceSeen.size}/${expectedPeers} ${presenceOk ? '✓' : '✗'}  ` +
        `signals: ${totalSignals}/${expectedSignals} ${signalsOk ? '✓' : '✗'}  ` +
        `types: [${Array.from(types).join(',')}]`,
    );
    if (!presenceOk || !signalsOk) allGood = false;
  }
  console.log('=============================');
  console.log(allGood ? '\nPASS — full-mesh delivery works.\n' : '\nFAIL — see ✗ rows above.\n');

  for (const peer of peers) peer.pool.close([RELAY]);
  process.exit(allGood ? 0 : 1);
}

function nameOf(peers, pk) {
  const p = peers.find((x) => x.pk === pk);
  return p ? `${p.name}(${pk.slice(0, 6)})` : pk.slice(0, 8);
}

function summarize(results) {
  const ok = results.filter((r) => r.status === 'fulfilled').length;
  const reasons = results
    .filter((r) => r.status === 'rejected')
    .map((r) => (r.reason instanceof Error ? r.reason.message : String(r.reason)));
  const why = reasons.length ? ` — ${[...new Set(reasons)].join(' | ')}` : '';
  return `${ok}/${results.length} accepted${why}`;
}

function sendRound(peers, type, seq, payloadSize = 256, big = false) {
  const blob = big ? BIG_BLOB : 'x'.repeat(payloadSize);
  for (const from of peers) {
    for (const to of peers) {
      if (from === to) continue;
      const payload = { type, seq, sdp: blob, ts: Date.now() };
      const ev = sign(from.sk, signalTemplate(CHANNEL_ID, to.pk, payload));
      void Promise.allSettled(
        from.pool.publish([RELAY], ev, { onauth: authSigner(from, `publish ${type}`) }),
      ).then((results) => log(from.tag, `→ ${type} seq=${seq} to ${to.name} (${summarize(results)})`));
    }
  }
}

run().catch((e) => {
  console.error('probe failed:', e);
  process.exit(1);
});
